package com.example.android.projectfanta;

/* A nutrition label refers to a specific named food */
public class NutritionLabel {
    String name;
    float calories;
    float caloriesFromFat;
    float totalFat;
    float satFat;
    float transFat;
    float sodium;
    float totalCarb;
    float fiber;
    float sugar;
    float protein;
    float vitC;
    float iron;
    float calcium;
    float magnesium;

    public void setName(String name) {
        this.name = name;
    }

    public void setCalories(float calories) {
        this.calories = calories;
    }

    public void setCaloriesFromFat(float caloriesFromFat) {
        this.caloriesFromFat = caloriesFromFat;
    }

    public void setTotalFat(float totalFat) {
        this.totalFat = totalFat;
    }

    public void setSatFat(float satFat) {
        this.satFat = satFat;
    }

    public void setTransFat(float transFat) {
        this.transFat = transFat;
    }

    public void setSodium(float sodium) {
        this.sodium = sodium;
    }

    public void setTotalCarb(float totalCarb) {
        this.totalCarb = totalCarb;
    }

    public void setFiber(float fiber) {
        this.fiber = fiber;
    }

    public void setSugar(float sugar) {
        this.sugar = sugar;
    }

    public void setProtein(float protein) {
        this.protein = protein;
    }

    public void setVitC(float vitC) {
        this.vitC = vitC;
    }

    public void setIron(float iron) {
        this.iron = iron;
    }

    public void setCalcium(float calcium) {
        this.calcium = calcium;
    }

    public void setMagnesium(float magnesium) {
        this.magnesium = magnesium;
    }
}
